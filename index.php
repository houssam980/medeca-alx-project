<!-- JavaScript to display the number of visitors increase -->
<script>
let visitorCount = 0;
let patientCount = 0;
let consultationCount = 0;
const targetVisitorCount = 56723; // Set your target visitor count

function updateVisitorCount() {
  const countElement = document.getElementById('visitor-count');
  const patientElement = document.getElementById('patient-count');
  const consultationElement = document.getElementById('consultation-count');
  const increment = Math.ceil(targetVisitorCount / 1000); // Increment by 1/1000 of the target count
  const interval = 30; // Interval in milliseconds

  const update = () => {
    visitorCount += increment;
    if (visitorCount >= targetVisitorCount) {
      visitorCount = targetVisitorCount;
      clearInterval(intervalId);
    }
    countElement.textContent = visitorCount.toLocaleString(); // Format with commas
    patientCount = Math.floor(visitorCount / 20); // Example: 1 patient per 20 visitors
    consultationCount = Math.floor(patientCount * 1.5); // Example: 1.5 consultations per patient
    patientElement.textContent = patientCount.toLocaleString();
    consultationElement.textContent = consultationCount.toLocaleString();
  };

  const intervalId = setInterval(update, interval);
}

updateVisitorCount();

</script>


<script>
 // JavaScript for dynamic testimonial cycling with fade effect
document.addEventListener("DOMContentLoaded", function () {
  const testimonials = document.querySelectorAll(".testimonial");
  let currentTestimonialIndex = 0;

  function showTestimonial(index) {
    // Fade out the current testimonial
    testimonials[currentTestimonialIndex].style.opacity = 0;

    // Fade in the new testimonial
    testimonials[index].style.opacity = 1;

    // Update current testimonial index
    currentTestimonialIndex = index;
  }

  function nextTestimonial() {
    let nextIndex = currentTestimonialIndex + 1;
    if (nextIndex >= testimonials.length) {
      nextIndex = 0; // Reset to the first testimonial
    }
    showTestimonial(nextIndex);
  }

  // Show the first testimonial initially
  showTestimonial(currentTestimonialIndex);

  // Automatically cycle through testimonials every 5 seconds
  setInterval(nextTestimonial, 2000);
});


</script>

